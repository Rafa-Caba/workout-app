import { useQuery } from "@tanstack/react-query";
import type { ApiError } from "@/api/httpErrors";
import { getPlanVsActual } from "@/services/planVsActual.service";
import { getRoutineWeek } from "@/services/workout/routines.service";
import { mergePlanVsActualPlanned } from "@/utils/pva/mergePlanVsActual";

export function usePlanVsActual(weekKey: string, enabled: boolean = true) {
    return useQuery<unknown, ApiError>({
        queryKey: ["planVsActual", weekKey],
        enabled: Boolean(weekKey) && enabled,
        queryFn: async () => {
            const [pva, routine] = await Promise.all([
                getPlanVsActual(weekKey),
                getRoutineWeek(weekKey).catch(() => null),
            ]);

            return mergePlanVsActualPlanned(pva as any, routine);
        },
        staleTime: 30_000,
    });
}
